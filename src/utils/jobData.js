// 职业名称到职业ID的映射
export const jobNameToId = jobs.reduce((map, job) => {
  map[job.name] = job.id;
  return map;
}, {});

// 职业缩写到职业ID的映射
export const jobAbbreviationToId = jobs.reduce((map, job) => {
  map[job.abbreviation] = job.id;
  return map;
}, {});

// 根据职业名称获取职业ID
export const getJobIdByName = (jobName) => {
  return jobNameToId[jobName] !== undefined ? jobNameToId[jobName] : null;
};

// 根据职业缩写获取职业ID
export const getJobIdByAbbreviation = (abbreviation) => {
  return jobAbbreviationToId[abbreviation] !== undefined ? jobAbbreviationToId[abbreviation] : null;
};

// 根据职业ID获取职业对象
export const getJobById = (jobId) => {
  return jobs.find(job => job.id === jobId) || null;
};

// 根据职业分组获取职业ID列表
export const getJobIdsByCategory = (category) => {
  return jobs
    .filter(job => job.category === category)
    .map(job => job.id);
};

// 获取所有职业ID
export const getAllJobIds = () => {
  return jobs.map(job => job.id);
}; 