// 创建世界ID到世界名称的映射
export const worldIdToName = {};
export const worldNameToId = {};

datacenters.forEach(datacenter => {
  datacenter.worlds.forEach(world => {
    worldIdToName[world.id] = world.name;
    worldNameToId[world.name] = world.id;
  });
});

// 创建世界ID到数据中心的映射
export const worldIdToDatacenter = {};

datacenters.forEach(datacenter => {
  datacenter.worlds.forEach(world => {
    worldIdToDatacenter[world.id] = datacenter.name;
  });
});

// 根据世界ID获取世界名称
export const getWorldNameById = (worldId) => {
  return worldIdToName[worldId] || `未知世界 (${worldId})`;
};

// 根据世界名称获取世界ID
export const getWorldIdByName = (worldName) => {
  return worldNameToId[worldName] || null;
};

// 根据世界ID获取数据中心名称
export const getDatacenterByWorldId = (worldId) => {
  return worldIdToDatacenter[worldId] || '未知数据中心';
};

// 根据区域获取数据中心列表
export const getDatacentersByRegion = (region) => {
  return datacenters.filter(datacenter => datacenter.region === region);
};

// 获取所有区域
export const getAllRegions = () => {
  return [...new Set(datacenters.map(datacenter => datacenter.region))];
};

// 获取所有数据中心名称
export const getAllDatacenterNames = () => {
  return datacenters.map(datacenter => datacenter.name);
};

// 获取所有世界名称
export const getAllWorldNames = () => {
  const allWorlds = [];
  datacenters.forEach(datacenter => {
    datacenter.worlds.forEach(world => {
      allWorlds.push(world.name);
    });
  });
  return allWorlds;
};

// 根据数据中心名称获取世界列表
export const getWorldsByDatacenter = (datacenterName) => {
  const datacenter = datacenters.find(dc => dc.name === datacenterName);
  return datacenter ? datacenter.worlds : [];
};